package com.pojo;

public class Rules
{
	private int id;
	private String rule_expression;
	private int group_id;
	private String rule_value;
	private String shuzu;
	private String yinchangyu;
	private String yinchangstr;
	public int getId()
	{
		return id;
	}
	public void setId(int id)
	{
		this.id = id;
	}
	public String getRule_expression()
	{
		return rule_expression;
	}
	public void setRule_expression(String rule_expression)
	{
		this.rule_expression = rule_expression;
	}
	public int getGroup_id()
	{
		return group_id;
	}
	public void setGroup_id(int group_id)
	{
		this.group_id = group_id;
	}
	public String getRule_value()
	{
		return rule_value;
	}
	public void setRule_value(String rule_value)
	{
		this.rule_value = rule_value;
	}
	public String getShuzu()
	{
		return shuzu;
	}
	public void setShuzu(String shuzu)
	{
		this.shuzu = shuzu;
	}
	public String getYinchangyu()
	{
		return yinchangyu;
	}
	public void setYinchangyu(String yinchangyu)
	{
		this.yinchangyu = yinchangyu;
	}
	public String getYinchangstr()
	{
		return yinchangstr;
	}
	public void setYinchangstr(String yinchangstr)
	{
		this.yinchangstr = yinchangstr;
	}
}
